// Vamos a realizar una comprobacion del ejercicio 1.3 de la relacion de problemas.
// Hay que ver el resultado de cada sentencia.

#include <iostream>
using namespace std;
int main (){
	double x,y; // Son los dos datos que vamos a calcular.
	x=5; y=2; // Valor de los dos datos.
	
	cout << "Valor de x: " << x << endl;
	cout << "Valor de y: " << y << endl;
	cout << "Valor de x/y\n: " << x/y;
	cout << "\tValor de y/x: " << y/x << endl;
	cout << "La mitad de x: " << x/2.0 << endl;
}
